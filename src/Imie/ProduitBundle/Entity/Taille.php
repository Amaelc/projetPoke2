<?php

namespace Imie\ProduitBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Taille
 */
class Taille
{
    /**
     * @var string
     */
    private $taille;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set taille
     *
     * @param string $taille
     * @return Taille
     */
    public function setTaille($taille)
    {
        $this->taille = $taille;

        return $this;
    }

    /**
     * Get taille
     *
     * @return string 
     */
    public function getTaille()
    {
        return $this->taille;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
}
